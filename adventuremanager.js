function prep_modal()
{
  $(".modalMulti").each(function() {

  var element = this;
	var pages = $(this).find('.modal-split');

  if (pages.length != 0)
  {
    	pages.hide();
    	pages.eq(0).show();

    	var b_button = document.createElement("button");
                b_button.setAttribute("type","button");
          			b_button.setAttribute("class","btn btn-primary");
          			b_button.setAttribute("style","display: none;");
          			b_button.innerHTML = "Back";

    	var n_button = document.createElement("button");
                n_button.setAttribute("type","button");
          			n_button.setAttribute("class","btn btn-primary");
          			n_button.innerHTML = "Next";
      
      var s_button = document.createElement("button");
                s_button.setAttribute("type","submit");
          			s_button.setAttribute("class","btn btn-primary btnColor");
                    s_button.setAttribute("style","display: none;");
          			s_button.innerHTML = "Create";

    	$(this).find('.modal-footer').append(b_button).append(n_button).append(s_button);
      
    	var page_track = 0;

    	$(n_button).click(function() {
        
            this.blur();

            if(page_track == 0)
            {
                $(b_button).show();
            }
            if(page_track == pages.length-2)
            {
                $(n_button).hide();
                $(s_button).show();
            }
//            if(page_track == pages.length-1)
//            {
//              $(element).find("form").submit();
//            }
            if(page_track < pages.length-1)
            {
                page_track++;

                pages.hide();
                pages.eq(page_track).show();
            }
    	});

    	$(b_button).click(function() {

    		if(page_track == 1)
    		{
    			$(b_button).hide();
                $(s_button).hide();
    		}

    		if(page_track == pages.length-1)
    		{
    			$(s_button).hide();
                $(n_button).show();
    		}

    		if(page_track > 0)
    		{
    			page_track--;

    			pages.hide();
    			pages.eq(page_track).show();
    		}
    	});
  }
  });
}